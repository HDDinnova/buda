        <h2 class="verdfosc text-center col-lg-3 col-md-4 col-lg-offset-9 col-md-offset-8">ESTIU</h2>
        <div class="clearfix"></div>
        <p class="verdfosc">Als mesos de juny i juliol, l’activitat principal que ocupava als buderos era mantenir nets els quadros nets de males herbes i paràsits, per tal que la collita donés una producció òptima. Per aconseguir-ho, segaven les herbes dels marges –cordons- que separaven els quadros, per treure a mans o amb l’ajuda de la falç aquelles que perjudicaven el creixement adequat de l’arròs, com la xufa, l’agram o el senill, i com més creixia l’arròs, cap a l’agost, el mill. En aquesta feina de birbar els arrossars hi jugaven un paper especial les dones, les birbadores que, com a la resta de la Ribera, col·laboraven molt intensament.</p>
