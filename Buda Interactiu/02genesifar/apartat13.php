        <p class="blanc col-lg-12 col-md-12 col-sm-12 col-xs-12">Després de plantejar-se diverses alternatives durant alguns anys, finalment, l’any 1978, el Servei de Senyals Marítims respon ordenant l'estudi d'una balisa oscil·lant. Finalment, el juny de 1980, en el port de Sant Carles de la Ràpita hi ha els equips necessaris per realitzar una plataforma marina, similar al d’una balisa oscil·lant, i de seguida es va dur endavant el projecte: una plataforma hexagonal de 16 m2 de superfície i 7,3 m d'altura, que suporta un trípode format per tres estaques d'acer que es claven en el fons fins una profunditat de 20 metres.<br>L’obra va ser feta per l'empresa Sotomar i va començar a instal·lar-se l'1 de març de 1983. Damunt de l'obra, La Maquinista Valenciana va aixecar una torreta formada per un castellet metàl·lic, dotada d'instal·lació fotovoltàica amb llanterna no visitable proveïda d'una òptica de 500 mm de diàmetre, de vidre polit, de llampades aïllades blanques i 14 milles d'abast i reflector de radar. Es va encendre per primer cop el 4 d'octubre de 1983.<br>Per a la seguretat en dies de boira, es va instal·lar una sirena de boira, que comença a funcionar al juliol de 1985, composta d'un amplificador Optimus ARB-25, un generador seqüencial i 3 altaveus de 15 W.</p>
