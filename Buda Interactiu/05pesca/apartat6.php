        <p class="blaufosc col-lg-6 col-md-6">Com diu el seu nom, la pesca de la “paupa” consisteix a anar palpant el fons sorrenc de la platja per localitzar els peixos després d’haver tirat una xarxa en forma de mitja lluna. Les orades, els llobarros i les llisses, els peixos més comuns a la zona, s’amaguen sota la sorra i quan es troben, se’ls ha d’agafar hàbilment amb la mà.<br>Els catxels, la tallarina i fins i tot el grúmol s’agafaven també amb molta facilitat a les llargues platges sorrenques de Buda, i es pagaven molt bé quan els venien als “arrieros” que posteriorment ho venien a les peixateries i sobretot als mercats de les grans ciutats.</p>
