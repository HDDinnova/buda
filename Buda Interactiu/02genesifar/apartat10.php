        <p class="marro col-lg-12 col-md-12 col-sm-12 col-xs-12">El deteriorament, però, era ja imparable. L’acció del mar sobre els pilotes i la regressió constant que sofria la franja costanera de l’Illa, van arribar a destruir les proteccions construïdes durant la dècada de 1950, la qual cosa va deixar el far totalment exposat a l’onatge i erosió marítima, aïllat dins del mar. L’oxidació i la força del mar colpejant l’estructura constantment, van començar a debilitar fortament les estructures de suport i els fonaments del Far. Ell seguia allí, plantat amb els seus 51 metres d’alt, però insegur, perquè no tardaria molt de temps a vinclar-se i caure sobre les aigües.</p>
