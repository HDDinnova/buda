        <p class="terra col-lg-12 col-md-12 col-sm-12 col-xs-12">Després de l’enfonsament del far metàl·lic de Buda quasi centenari i del de formigó que va durar 3 anys, es van debatre possibles solucions, però en primer lloc s’havia de prendre una decisió ràpida per il·luminar el cap marítim del Delta.<br>Així que es va aixecar en la desembocadura del riu Ebre un castellet tubular metàl·lic de 21 metres d'altura, pintat a bandes blanques i negres i dotat d'una instal·lació de gas acetilè que se substituiria posteriorment per una altra de fotovoltaica. Al peu del castellet es va construir una caseta rectangular pintada de blanc per allotjar primerament els acumuladors i, més tard, les bateries del sistema fotovoltaic i els equips del primer sistema de control remot.</p>
