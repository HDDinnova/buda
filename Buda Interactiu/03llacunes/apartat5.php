        <p class="negre col-lg-5 col-md-5 col-sm-10 col-xs-10 text-center"><strong>PROGRAMA DE CRIA</strong></p>
        <div class="clearfix"></div>
        <p class="taronga col-lg-12 col-md-12">Les tortugues provenen del programa de cria en captivitat que realitza el Centre de Fauna Salvatge del Canal Vell (PNDE). Després de passar tres 
        anys en les instal·lacions de cria, l’abril de 2012 es van portar les tortugues al tancat d’aclimatació de l’illa de Buda on van romandre durant 
        cinc mesos. Aquest període d’aclimatació té per objectiu fixar-les a aquesta llacuna i evitar que, un cop s’alliberin, marxin fora de la zona. 
        Durant aquests mesos, dins el tancat d’aclimatació els tècnics del Parc i de Forestal Catalana han portat a terme un control mensual de l’estat 
        dels exemplars.</p>                
