        <p class="blanc col-lg-12 col-md-12 col-sm-12 col-xs-12"><strong>ELS ESCULLS ARTIFICIALS  A LA MAR DE L’EBRE</strong></p>
        <div class="clearfix"></div>
        <p class="blanc col-lg-12 col-md-12 col-sm-12 col-xs-12">Un escull artificial és un conjunt d’objectes dipositats expressament en el fons marí, i que segons la forma o el pes que tenen, o pel lloc on s’instal·len, poden fer funcions similars als esculls naturals, biòtops que formen els niells de roca submarins.<br><br>Tradicionalment, s’han utilitzat diferents tipus d’elements per fer fer d’esculls artificials: camions, cotxes, canonades de ferro, ciment, etc. A Catalunya, durant els anys setanta, en molts llocs de la costa, els pescadors omplien bidons de 200 litres de pedres, ciment i barres de ferro, i els fondejaven en llocs estratègics. Avui dia, els esculls artificials són més grans, generalment grans estructures de formigó de diferents formes i volums, reforçades amb barres de ferro.</p>
