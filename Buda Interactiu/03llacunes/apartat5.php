        <p class="taronga col-lg-5 col-md-5 col-sm-10 col-xs-10 text-center"><strong>El seguiment</strong></p>
        <div class="clearfix"></div>
        <p class="blanc col-lg-12 col-md-12">El seguiment posterior d’aquests individus és molt important, ja que és l’única manera de determinar la supervivència de les tortugues i la consolidació 
        d’aquesta nova població. Totes les tortugues estan marcades individualment amb microxips implantats sota la pell (com els que s’utilitzen per a les 
        mascotes), un codi d’osques a les plaques de la closca (sistema habitualment utilitzat per marcar les tortugues) i, a més a més, uns quants d’aquests 
        exemplars estan equipats amb emissors de radioseguiment. Aquests emissors permeten localitzar-les i determinar-ne l’adaptació a les condicions en llibertat. 
        En aquesta fase del seguiment en llibertat també participen tècnics de l’IRTA.</p>                
