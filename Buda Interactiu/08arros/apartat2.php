        <h2 class="blanc text-center col-lg-4 col-md-4">TARDOR</h2>
        <div class="clearfix"></div>
        <p class="blanc">Al mes de setembre, començava altre cop a bullir tota l’illa d’activitat frenètica: tocava la sega de l’arròs daurat i madur, ajudats de la falç i el sorrac. Calia  haver deixat ben eixuts d’aigua els quadros per facilitar la sega.<br>La sega esdevenia una litúrgia de feines ben seguides i ordenades, dutes a terme per tota una munió d’homes que havien d’anar ben coordinats perquè la collita anés bé, des que agafaven lo guaix d’arròs amb una mà per segar-la amb cura amb l’altra, fins que altres homes passaven a recollir les garbes d’arròs segat i lligat en feixos que s’havien deixat a sobre del rostoll de la planta.</p>
