        <h2 class="turquesa col-lg-5 col-md-5 col-sm-12 col-xs-12"><strong>LA MORT DEL FAR DÓNA VIDA AL MAR</strong></h2>
        <div class="clearfix"></div>
        <p class="turquesa col-lg-8 col-md-8 col-sm-12 col-xs-12">El juliol de l’any 1969, el “Cuerpo de Buceadores de Combate del Centro de Instrucción de la Marina”, amb seu a Cartagena, es van desplaçar a l’illa de Buda per fer volar les restes del Far de Buda, tombat al mar des de desembre de 1961, és a dir, després de quasi 8 anys submergit en les aigües de la Mar de l’Ebre.<br><br>“El Faro era un estorbo para la navegación y, además, un peligro”, deia el NO-DO  de l’època.  Amb l’explosió, el Far queda destruït i desparramat per una gran superficie, i les seves parts (tirants, llanterna, columnes de ferro de la base, els cargols Mitchell, …) es van anar dipositant sobre els fons sorrencs del delta.<br><br>Aquest fet fortuït va produir que, amb el pas del temps, les restes del far anessin formant cavitats, que han permès que diferents espècies de peixos i mol•luscs de la mar de l’Ebre puguen reproduir-se i crèixer en un lloc protegit, on no s’hi pot practicar pesca d’arrossegament o altres modalitats pesqueres.<br><br>El Far de Buda sota les aigües ha esdevingut així un escull marí artificial, i per tant, una àrea d’interès biològic. Símbol de seguretat i refugi per a mariners i pescadors mentre va estar en actiu, és ara també lloc de recer i vida per peixos i mol·luscs: orades (“moixarres”), escòrpores i altres peixos, i profusió de pops, que en les seves restes troben llocs idonis on guarir-se i reproduir-se.</p>
